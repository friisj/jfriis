/**
 * Get a readable label for a shade
 */
export function getShadeLabel(shade: Shade): string {
  const labels: Record<Shade, string> = {
    50: '50 (Lightest)',
    100: '100',
    200: '200',
    300: '300',
    400: '400',
    500: '500 (Base)',
    600: '600',
    700: '700',
    800: '800',
    900: '900',
    950: '950 (Darkest)',
  }
  return labels[shade]
}

/**
 * Get a readable label for a scale
 */
export function getScaleLabel(scale: TailwindScale): string {
  return scale.charAt(0).toUpperCase() + scale.slice(1)
}
